from timeit import timeit
from cProfile import run


def revers(enter_num, revers_num=0):
    if enter_num == 0:
        return
    else:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
        revers(enter_num, revers_num)


def revers_2(enter_num, revers_num=0):
    while enter_num != 0:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
    return revers_num


def revers_3(enter_num):
    enter_num = str(enter_num)
    revers_num = enter_num[::-1]
    return revers_num


number = int(input('Введите число: '))

print('Рекурсия', timeit('revers(number)', globals=globals(), number=1000))
print('Цикл', timeit('revers_2(number)', globals=globals(), number=1000))
print('Срез', timeit('revers_3(number)', globals=globals(), number=1000))

run('revers(1000000000)')
run('revers_2(1000000000)')
run('revers_3(1000000000)')

